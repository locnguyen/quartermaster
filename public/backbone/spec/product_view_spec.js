describe('Product Views', function() {
  describe('A view for a new product', function() {
     it('should require a manufacturer');

     it('should require a model name');

     it('should have a field for notes');
  });

  describe('A view for a product', function() {
    it('should show the manufacturer');

    it('should show the model name');

    it('should show notes');

    it('should show the number of assets');
  });

  describe('A view to search for products', function() {
    it('should have a field to enter a search string');

    it('should display a list of results after a search');
  });
});